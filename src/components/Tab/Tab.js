const useStyles = makeStyles(theme => ({
  root: {
    flexGrow: 1,
    backgroundColor: theme.palette.background.paper,
    display: 'flex',
    height: 224,
  },
  tabs: {
    borderRight: `1px solid ${theme.palette.divider}`,
  },
}));

function VerticalTabs(props) {
  const classes = useStyles();
  const [value, setValue] = React.useState(0);

  function handleChange(event, newValue) {
    setValue(newValue);
  }
  function toAll() {
    props.history.push({ pathname: `/mybook/all` });
  }
  function toRead() {
    props.history.push({ pathname: `/mybook/read` });
  }
  function reading() {
    props.history.push({ pathname: `/mybook/reading` });
  }
  function willRead() {
    props.history.push({ pathname: `/mybook/willread` });
  }

  return (
    <div className={classes.root}>
      <Tabs
        orientation="vertical"
        variant="scrollable"
        value={value}
        onChange={handleChange}
        aria-label="Vertical tabs example"
        className={classes.tabs}
      >
        <Tab label="all" className='read' onClick={toAll}/>
        <Tab label="read" onClick={toRead}/>
        <Tab label="reading" onClick={reading}/>
        <Tab label="willread" onClick={willRead}/>
      </Tabs>
    </div>
  );
}

export default withRouter(VerticalTabs)